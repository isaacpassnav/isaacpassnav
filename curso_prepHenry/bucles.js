
// Bucle FOR cuando SI se sabe los pasos para la ejecucion
var suma=0;

for(var i=0; i<5; i++){
    suma=suma+i;
    console.log("Variable de iteracion:", i);
}
console.log("Variable suma:", suma);
// Bucle WHILE .... cuando NO se sabe los pasos para la ejecucion 

while(suma<3){
    suma=suma+1;
    console.log(suma);
}
